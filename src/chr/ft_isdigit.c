/*
Checks for a digit (0..9).
Decimal: 48..57;
Octal: 0o60..0o67 && 0o70..0o71;
Hex: 0x30..0x39;
*/
int	ft_isdigit(int a)
{
	return (a >= '0' && a <= '9');
}
